public class StringExercise {
    public static void main(String[] args) {
        String x = "We don't need no education\nWe don't need no thought control";
        System.out.println(x);
        String y = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(y);
        String z = "In windows. the main drive is usually C:\\";
        System.out.println(z);
        String a = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(a);
    }
}

